export async function getMelvinAnalysis(answers, previousSessions = []) {
  const answersText = formatAnswersForPrompt(answers);
  const growthContext = previousSessions.length > 0
    ? `\n\nPREVIOUS ASSESSMENT HISTORY (${previousSessions.length} prior session${previousSessions.length !== 1 ? 's' : ''}):\n${formatPreviousSessions(previousSessions)}`
    : '';

  const messages = [
    {
      role: 'user',
      content: `Here are Melvin's complete assessment answers:\n\n${answersText}${growthContext}\n\nPlease provide his full analysis. Be specific to what he actually said. This is real — build it for him.`,
    },
  ];

  const response = await fetch('/.netlify/functions/claude', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      userId: 'melvin',
      systemPrompt: `${MELVIN_SYSTEM_PROMPT}\n\n${MELVIN_AI_PERSONA_RULES}`,
      messages,
    }),
  });

  if (!response.ok) throw new Error('Failed to get analysis');
  const data = await response.json();
  return data.content;
}

function formatAnswersForPrompt(answers) {
  return Object.entries(answers)
    .map(([questionId, answer]) => `[${questionId}]: ${JSON.stringify(answer)}`)
    .join('\n');
}

function formatPreviousSessions(sessions) {
  return sessions.map((s, i) => {
    const date = new Date(s.createdAt?.toDate?.() || s.createdAt).toLocaleDateString();
    return `Session ${i + 1} (${date}):\n${s.summary || s.results?.slice(0, 500) || 'No summary available'}`;
  }).join('\n\n');
}
