export async function getMekhiAnalysis(answers, previousSessions = []) {
  const answersText = formatAnswersForPrompt(answers);
  const academicContext = buildMekhiContextBlock();
  const growthContext = previousSessions.length > 0
    ? `\n\nPREVIOUS ASSESSMENT HISTORY (${previousSessions.length} prior session${previousSessions.length !== 1 ? 's' : ''}):\n${formatPreviousSessions(previousSessions)}`
    : '';

  const messages = [
    {
      role: 'user',
      content: `Here are Mekhi's complete assessment answers:\n\n${answersText}\n\n${academicContext}${growthContext}\n\nPlease provide his full analysis. Cross-reference his self-reported answers against the real academic data above — where they contradict, the real data wins. Be specific to what he actually said AND what the numbers actually show. Build this for him. He is real.`,
    },
  ];

  const response = await fetch('/.netlify/functions/claude', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      userId: 'mekhi',
      systemPrompt: `${MEKHI_SYSTEM_PROMPT}\n\n${MEKHI_AI_PERSONA_RULES}`,
      messages,
    }),
  });

  if (!response.ok) throw new Error('Failed to get analysis');
  const data = await response.json();
  return data.content;
}

function formatAnswersForPrompt(answers) {
  return Object.entries(answers)
    .map(([questionId, answer]) => `[${questionId}]: ${JSON.stringify(answer)}`)
    .join('\n');
}

function formatPreviousSessions(sessions) {
  return sessions.map((s, i) => {
    const date = new Date(s.createdAt?.toDate?.() || s.createdAt).toLocaleDateString();
    return `Session ${i + 1} (${date}):\n${s.summary || s.results?.slice(0, 500) || 'No summary available'}`;
  }).join('\n\n');
}
